  final urls = ['http://clips.vorwaerts-gmbh.de/big_buck_bunny.mp4'];
  @override
  void initState() {
    super.initState();
    Downloader.listener.add((taskId, state, totalSize, progress, speed) {
      if (!mounted || taskId != taskId) return;
      setState(() {
        _taskId = taskId;
        _state = state;
        if (_state == DownloadState.download) {
          _totalSize = totalSize;
        }
        _progress = progress;
        _speed = speed;
      });
    });
    getTemporaryDirectory().then((tempDir) {
      String tempPath = tempDir.path;
      _downloader.init(tempPath);
      _downloader
          .download(Task(id: _taskId, urls: urls, totalSize: _totalSize,needSplit: true,needCalculate: false));
    });
  }

  @override
  Widget build(BuildContext context) {
    return GestureDetector(
      onTap: () {
        switch (_state) {
          case DownloadState.pause:
            {
              _downloader.download(
                  Task(id: _taskId, urls: urls, totalSize: _totalSize));
              break;
            }
          case DownloadState.unknown:
            break;
          case DownloadState.pre:
            _downloader.pause(_taskId);
            break;
          case DownloadState.download:
            _downloader.pause(_taskId);
            break;
          case DownloadState.complete:
            break;
          case DownloadState.failed:
            _downloader
                .download(Task(id: _taskId, urls: urls, totalSize: _totalSize));
            break;
        }
      },
      child: Container(
        width: double.infinity,
        decoration: BoxDecoration(
          color: Colors.grey,
          borderRadius: BorderRadius.circular(16),
        ),
        child: Column(
          children: [
            Text('任务id: $_taskId'),
            const SizedBox(
              height: 2,
            ),
            Text('下载状态: $_state'),
            const SizedBox(
              height: 2,
            ),
            Text(
                '文件总大小: ${(_totalSize / 1024).toDouble().toStringAsFixed(2)}kb'),
            const SizedBox(
              height: 2,
            ),
            Text('下载进度: $_progress'),
            const SizedBox(
              height: 2,
            ),
            Text('下载速度: ${(_speed / 1024).toDouble().toStringAsFixed(2)}kb/s'),
          ],
        ),
      ),
    );
  }
